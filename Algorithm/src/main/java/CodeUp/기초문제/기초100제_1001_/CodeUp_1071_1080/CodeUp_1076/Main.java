import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        char chr = sc.next().charAt(0);

        for(char c = 'a'; c <= chr; c++) {
            System.out.print(c + " ");
        }
    }
}

